async function main() {
  console.log(`Start seeding ...`);
  for (const f of foodData) {
    const existingFood = await prisma.food.findFirst({
      where: {
        name: f.name,
        creatorId: null,
      },
    });

    if (!existingFood) {
      const food = await prisma.food.create({
        data: f,
      });
      console.log(`Created food with id: ${food.id}`);
    } else {
      // Actualizar todos los campos del alimento global existente
      const updated = await prisma.food.update({
        where: { id: existingFood.id },
        data: f,
      });
      console.log(`Updated food '${f.name}' with new nutrient data.`);
    }
  }
  console.log(`Seeding finished.`);
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
